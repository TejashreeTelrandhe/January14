public class If_Elsecontrolstatementpractice {

	public static void main(String[] args) {
     //  If Statement true it will execute
	 int a = 21;
	 if(a>=18) {
		 System.out.println("Eligible for voting");
	 }
	 else {
		 System.out.println("Not eligible for voting");
	 }
	 System.out.println();
	 
	//   If else statement is false then else statement will  execute
	int i= 101;
	 if(i<=100) {
		 System.out.println("i is less than or equals to 100");
	 }
	 else {
		 System.out.println("i is greater than or equals to 100");
	 }
		}

}
