package LogicalPgm_19Dec;

public class Swapping_Two_Numbers {

	public static void main(String[] args) {
		int a = 44;
		int b = 55;
		int c;
		System.out.println("Before swapping numbers a = " + a + " and b = " + b);
		c = a; // c = 44
		a = b; // a= 55
		b = c; // b=45
		System.out.println("After swapping numbers a = " + a + " and b = " + b);
		
	}

}
